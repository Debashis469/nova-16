module Mux4Way16 (
    input [15:0] a,
    input [15:0] b,
    input [15:0] c,
    input [15:0] d,
    input [1:0] sel,
    output [15:0] out
);
    wire [15:0] muxAB, muxCD;

    Mux16 inst1 (.a(a), .b(b), .sel(sel[0]), .out(muxAB));
    Mux16 inst2 (.a(c), .b(d), .sel(sel[0]), .out(muxCD));
    Mux16 inst3 (.a(muxAB), .b(muxCD), .sel(sel[1]), .out(out));
endmodule